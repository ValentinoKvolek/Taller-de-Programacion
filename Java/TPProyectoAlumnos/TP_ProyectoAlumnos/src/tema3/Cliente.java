package tema3;

/**
 *
 * @author valen
 */
public class Cliente {
    
    private String Nombre;
    private int Dni;
    private int Edad;
    
    
    
    public Cliente(String Nombre, int Dni, int Edad) {
        this.Nombre = Nombre;
        this.Dni = Dni;
        this.Edad = Edad;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public int getDni() {
        return Dni;
    }

    public void setDni(int Dni) {
        this.Dni = Dni;
    }

    public int getEdad() {
        return Edad;
    }

    public void setEdad(int Edad) {
        this.Edad = Edad;
    }
    
    
    
}
